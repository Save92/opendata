        <h2>Les enjeux</h2>

        <p>Dans le domaine scientifique, il est essentiel de partager les informations. Pouvoir reconnaître la qualité de  biens collectifs aux résultats de la recherche devient donc un impératif. Quant au Web de données, autre exemple retenu, il implique que l’on puisse relier et interroger de manière simple les données dispersées sur le web, mais aussi d’en disposer sans se heurter à des obstacles juridiques.</p>
        <p>Or,  pour accéder à des  données et avoir le droit de les utiliser, on s’aperçoit qu’il faut bien  souvent contacter les ayants droit et négocier des licences, ce qui s’avère généralement lent et coûteux.</p>
        <p>Pour pallier les risques croissants de réappropriation des données, liés à  l’expansion actuelle de la propriété intellectuelle, ont été développées parallèlement, depuis plusieurs années, des approches techniques et juridiques dites « ouvertes ».</p>
        <p>Les données libres et ouvertes permettent ainsi de disposer d’un fonds commun de données dans lequel n’importe qui peut puiser et ce, malgré les différences importantes des contextes juridiques et institutionnels de leur création.</p>

        <h2>Quelques rappels</h2>

        <p>Définitions</p>
        <dl>
            <dt>donnée</dt>
            <dd>: un fait, notion ou instruction représentée sous forme conventionnelle convenant à la communication, l’interprétation ou au traitement par des moyens humains ou automatiques (afnor)</dd>

            <dt>donnée publique</dt>
            <dd>: donnée collectée ou produite dans le cadre de sa mission, par un service public, sur des fonds publics.</dd>

            <dt>donnée libre</dt>
            <dd>: une donnée que l’on est libre d’utiliser, de modifier et de rediffuser</dd>

            <dt>donnée ouverte</dt>
            <dd>: l’ouverture s’applique la notion d’interopérabilité, assurée par des standards.</dd>

            <dt>licence libre</dt>
            <dd>: contrat par lequel un titulaire d’un droit de propriété intellectuelle concède à un tiers tout ou une partie de la jouissance d’un droit, en accordant au moins la possibilité de modifier, de rediffuser et
                de réutiliser l’œuvre dans des <a href="http://fr.wikipedia.org/wiki/%C5%92uvre_d%C3%A9riv%C3%A9e">œuvres dérivées</a>.
                Ces libertés peuvent êtres soumises à conditions
                (<a href="http://fr.wikipedia.org/wiki/Licence_libre">Wikipédia</a>).
            </dd>

            <dt>domaine public</dt>
            <dd>: des œuvres protégées par le droit d’auteur dont la durée des droits patrimoniaux a expiré (droit d’auteur) ou des choses communes qui n’appartiennent à personne mais dont l’usage est commun à tous  (art. 714 Code civil).</dd>
        </dl>

        <h2>Le cadre juridique</h2>

        <p>Le droit d’auteur. Les données « brutes » ne sont  pas protégées par le droit d’auteur, mais leur mise en forme (sous forme de graphiques, diagrammes,  etc.) pourrait l’être, dès lors que celle-ci s’avère originale et que la présentation des données ne découle pas automatiquement de l’utilisation d’un procédé technique ou d’un logiciel. Si, par ailleurs, la structure et choix des données sont originaux, la base de données sera protégée par le droit d’auteur.</p>
        <p>Le droit sui generis des bases de données. Si le producteur de la base de  données peut en outre prouver avoir investi de manière substantielle pour constituer et gérer sa base, il peut s’opposer à toute extraction ou réutilisation substantielle de celle-ci.</p>
        <p>Le droit de réutilisation des données publiques. Dans ce cadre, l’administration met à la disposition du public les données qu’elle a produites ou qu’elle détient.</p>
        <p>
            D’autres mécanismes.
