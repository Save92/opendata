            par un contrat et a posteriori par une action en concurrence déloyale,
            pour parasitisme ou pour <a href="http://www.dictionnaire-juridique.com/definition/de-in-rem-verso.php">enrichissement sans cause</a>.
        </p>

        <h2>Libérer les données</h2>
        <h3>Pourquoi adopter une licence ?</h3>

        <p>Même dans le cas où les données ne sont pas protégées par un droit (droit d’auteur, droit sui generis du producteur de base de données), il est recommandé de les diffuser accompagnées d’un contrat de  licence.</p>
        <p>Les utilisateurs ont, en effet, besoin de savoir d’où viennent les données (notion d’attribution), si celles-ci elles ont été modifiées (souci d’intégrité et de validation) et de connaître toutes les modalités de réutilisation autorisées.</p>
        <p>Les recours à des contrats de licences reconnus dans le monde entier, même si celles-ci nécessitent une adaptation au droit national, permet d’organiser le partage des données en évitant,  puisque les contrats sont la loi des parties, de se pencher sur le problème de la loi applicable et de se heurter à des concepts juridiques différents.</p>

        <h3>L’extrême diversité des licences</h3>

        <p>Dans son rapport, le Conseil supérieur de la propriété littéraire et artistique (CSPLA) note qu’il existe des œuvres sous licences mixtes (contenant des éléments sous licences libres et d’autres sous contrats propriétaires) ou encore des licences doubles (permettant d’opter soit pour une licence libre qui impose certaines contraintes, soit pour une licence propriétaire).</p>
        <p>À côté des « domaines historiques d’expansion » que sont les logiciels libres, des modèles « généralistes » (licences Creative Commons ou Art Libre, par exemple), diverses communautés (informaticiens, chercheurs, musiciens, …) ont développé des licences qui répondent à leurs priorités. En outre, afin de les améliorer, pour s’adapter à de nouvelles pratiques ou de nouvelles législations, ces licences ont  pu évoluer dans le temps  et se présenter sous de nouvelles versions numérotées.  Le mouvement ne s’est étendu qu’ensuite et  plus récemment aux données.</p>

        <h3>Quelle licence choisir ?</h3>

        <p>Pour s’assurer que les données restent libres et bâtir un fonds commun de données,  il est important de  pouvoir contrôler leurs usages. Abandonner tous ses droits de manière anticipée, en choisissant une licence qui fait  entrer son œuvre dans le domaine public (comme l’Open Data Commons – Public Domain Dedication & Licence (PPDL) ou la licence CC Zéro), permet certes de les utiliser pour créer des œuvres dérivées, mais  ne répond pas obligatoirement à un tel objectif, puisqu’il n’oblige pas à partager les résultats obtenus à partir des données utilisées. N’importe qui, une entreprise par exemple, pourrait se réapproprier ces données, d’où le paradoxe qui consiste à créer un droit de propriété, qui ne devrait pas exister, pour empêcher cette action.</p>
        <p>Si l’on entend bâtir un fonds commun de collaboration, il s’avère de ce fait   prudent d’adopter une logique de  Copyleft qui « force » le partage. Classées dans le rapport du CSPLA parmi  les licences offrant une liberté pérenne, les licences Copyleft ne « se limitent pas à l’octroi du droit d’utiliser, de modifier et de diffuser l’oeuvre : elles veillent à ce que l’usage de l’œuvre copiée ou modifiée demeure libre. Dès lors que sont combinés les éléments de l’oeuvre mise à disposition, l’oeuvre seconde devra obéir au même régime », ce qui permet de s’assurer de l’existence d’un fonds commun d’oeuvres que l’on peut utiliser librement.</p>
        <p>La licence Creative Commons BY-SA qui oblige à citer la source et à partager les données obtenues à partir des données utilisées sous la même licence CC BY-SA, a été adoptée notamment par les concepteurs du projet OpenStreetMap, projet qui connaît un grand succès, qui vise à cartographier des villes à partir de données géographiques libres. Considérant toutefois qu’une licence CC vise à protéger des œuvres protégées par le droit d’auteur, les concepteurs d’OpenStreetMap songent à adopter la licence ODbL, adaptée au droit sui generis des bases de données</p>

        <h3>Le choix d’une licence n’est pas neutre</h3>

        <p>S’il convient, en préalable, de s’assurer que l’on est en mesure de céder tous les droits que la licence va conférer (coauteurs éventuels, droits d’auteur sur les œuvres incorporées, …), il convient aussi  d’en apprécier l’impact avant d’opter pour l’une d’entre elles.</p>
